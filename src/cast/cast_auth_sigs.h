// IMPORTANT: This file contains placeholder signatures and a placeholder peer
// certificate. Chrome will reject Cast authentication until real signatures are
// extracted from the AirReceiver APK and the matching peer certificate is embedded.
//
// Extraction process:
//   1. Download AirReceiver APK from the Play Store.
//   2. Decompile with apktool or jadx.
//   3. Locate the precomputed 256-byte RSA-2048 signature table in the assets or
//      native library.
//   4. Export as a C array: xxd -i signatures.bin
//   5. Paste the 795x256 byte array into kCastAuthSignatures below.
//   6. Extract the matching DER-encoded peer certificate from the same APK
//      and paste into kCastAuthPeerCert.
//
// Reference: https://xakcop.com/post/shanocast/
//            https://tristanpenman.com/blog/posts/2025/03/22/chromecast-device-authentication/

