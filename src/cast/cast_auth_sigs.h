// Returns the precomputed 256-byte signature for the current 48-hour window.
// unixTimestamp: seconds since epoch (e.g. from QDateTime::currentSecsSinceEpoch()).
inline const uint8_t* getCastAuthSignature(uint64_t unixTimestamp) {
    // 172800 = 48 * 60 * 60 seconds per window.
    size_t index = (unixTimestamp / 172800) % kCastAuthSignatureCount;
    return &kCastAuthSignatures[index][0];
}

